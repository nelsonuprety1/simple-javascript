let myAge = 22; //my age
let earlyYears = 2; //value will be changed
earlyYears = 10.5 * 2;
let laterYears = (myAge - 2) * 4; //subtracting 2 from myAge and multiplying it by 4
let myAgeInDogYears = earlyYears + laterYears; //adding and storing values
